function assertSameShape(base, candidate, path = "") {
  for (const key of Object.keys(base)) {
    const nextPath = path ? `${path}.${key}` : key;
    if (!(key in candidate)) {
      throw new Error(`Missing key ${nextPath}`);
    }
    if (
      typeof base[key] === "object" &&
      base[key] !== null &&
      !Array.isArray(base[key])
    ) {
      assertSameShape(base[key], candidate[key], nextPath);
    }
  }
}

